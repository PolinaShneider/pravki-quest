                            '–õ–∞–¥–Ω–æ, –±–æ–ª—å—à–µ –Ω–µ –±—É–¥—É –º—É—á–∏—Ç—å —Ç–µ–±—è. –¢–µ–±–µ –µ—â–µ –ø—Ä–µ–¥—Å—Ç–æ–∏—Ç —Ö–æ—Ä–æ—à–µ–Ω—å–∫–æ –ø–æ—Ä–∞–±–æ—Ç–∞—Ç—å —Å –º–∞–∫–µ—Ç–æ–º. –ü–æ–∏—â–∏ –≤ –∏–Ω—Ç–µ—Ä–Ω–µ—Ç–µ, –∫–∞–∫ —Å –ø–æ–º–æ—â—å—é –∫–æ–º–∞–Ω–¥ –≤ –∫–æ–Ω—Å–æ–ª–∏ —Å–∫–∞—á–∏–≤–∞—Ç—å —Ñ–∞–π–ª—ã. –ù–µ –∑–∞–±—ã–≤–∞–π, —á—Ç–æ —É –Ω–∞—Å –ª—é–±—É—é –∫–æ–º–∞–Ω–¥—É –Ω—É–∂–Ω–æ –≤–≤–æ–¥–∏—Ç—å, –Ω–∞—á–∏–Ω–∞—è —Å–æ —Å–ª—ç—à–∞. –ó–∞–æ–¥–Ω–æ –∏ –ø—Ä–æ–≤–µ—Ä–∏—à—å —Å–µ–±—è'
